      FUNCTION EXPTRAP(X)
     0 DATA  XMIN, XMAX, EMIN, EMAX
     1    / -70., 70., 0., 2.51544E 30 /
      IF (X .LE. XMIN) GO TO 1
      IF (X .GE. XMAX) GO TO 2
      EXPTRAP = EXP(X)
      RETURN
  1   EXPTRAP = EMIN
      RETURN
  2   EXPTRAP = EMAX
      RETURN
      END
c
c    compute response for model with q1(t) = Vb Ca(t);
c    i. e., ignore effects of flow
C    COMPUTE ONLY FOR COMPARTMENTS 2-4
C    Q1 MUST BE ADDED IN CALLING PROGRAM and multiply
C    by Vb
C    JOANNE MARKHAM
C    JULY 1991
c
       subroutine noflowg (n,t,rate,DECAY,QT)
       dimension t(2),rate(6),qt(2)
       xk21 = rate(2)
       xk12 = rate(3)
         xk32 = rate(4)
       xk43 =rate(5)
       xk04 = rate(1)
       xk22 = xk12 + xk32
       cona = xk22 - xk43
       conb = xk22 - xk04
       conc = xk04 - xk43
       con1 = (xk21*xk32)/cona
       cond = con1*xk43
       con2 = cond/conb
       con3 = cond/conc
       do 100 i=1,n
       tt = t(i)
         exa = exptrap(-xk22*tt)
       exb = exptrap(-xk43*tt)
       exc = exptrap(-xk04*tt)
       q2 = xk21*exa
       q3 = con1*(exb-exa)
       q4 = con2*(exa -exc) + con3*(exb - exc)
       qt(i)  = q2 + q3 + q4
100      continue
C
C   CORRECT FOR DECAY IF NECESSARY
C
         IF (DECAY.EQ.0.) RETURN
         RC = ALOG(2.0)/DECAY
         DO 200 I=1,N
         QT(I) = QT(I) *EXP(-RC*T(I))
 200     CONTINUE
       return
       end
c                                                                               
c    scale matrix then compute inverse                                          
c     use  svd for matrix inversion                                             
c    Double precision version                                                   
c                                                                               
c    joanne markham oct 1991                                                    
c                                                                               
       subroutine svdinv (n,a,b,det)                                            
       double precision a(10,10),b(10,10),det,w(10),u(10,10),v(10,10),          
     1diag(10),dtemp, tol,wmin,wmax                                                            
c                 
c   move matrix                                                                
c                                                                               
        do 20 i =1,n                                                            
	do 20 j= 1,n                                                                   
20      u(i,j) = a(i,j)
c        write (6,*) ' d ',(diag(i),i=1,n)                                       
c                                                                               
c    for accuracy set diag elements to 1.                                       
c                                                                               
c                                                                               
c   call svd                                                                    
c                                                                               
	call svdcmp ( u, n, n, 10,10,w,v)                                              
c                                                                               
c     check condition number and set small singular values to zero              
c                                                                               
         wmax =0.                                                               
	 wmin = 999999.                                                                
        det = 1.                                                                
	do 50 i=1,n                                                                    
         det = det *w(i)                                                        
	 if (w(i) .gt. wmax)  wmax = w(i)                                              
	 if (w(i) .lt. wmin) wmin = w(i)                                               
50       continue          
	 write (6,*) ' SINGULAR VALUES ', (w(i),i=1,n)
         if (wmin.ne.0.) then                                                   
	 cno = wmax /wmin                                                              
	 write (6,*) ' condition no = ', cno                                           
	 else                                                                          
	 write (6,*)' DET = 0. '                                                       
         endif                                                                  
	 tol =  wmax *1.0d-7                                                           
c                                                                               
c                                                                               
c     compute inverse from u , w and v                                          
c                                                                               
c                                                                               
           do 60 i=1,n                                                          
	       if (w(i) .lt. tol) then                                                 
	       w(i) = 0.                                                               
	       else                                                                    
                                                                                
	       w(i) = 1.0/w(i)                                                         
	       endif                                                                   
60       continue                                                               
                                                                                
 	 do 80 i= 1,n                                                                 
	  do 80 j = 1, n                                                               
          dtemp = 0.                                                            
	  do 70 k = 1,n                                                                
70       dtemp = dtemp + v(i,k)*u(j,k)*w(k)                                     
c        write (6,1002) i, j, dtemp                                             
	 b(i,j) = dtemp                                                                
80        continue	                                                             
1002      format (2i5,f20.6)                                                    

       	return                                                                  
	 end                                                                           
c                                                                               
c                                                                               
c    Singular value decomposition                                               
c    page 60 of Numerical Recipes  (WH Press, BP Flannery, SA Teukolsky, and    
c    WT Vetterling)   Cambridge University Press                                
c                                                                               
c    Joanne Markham  Oct 1991                                                   
c                                                                               
       Subroutine SVDCMP (A, M, N, MP, NP, W, V)                                
c                                                                               
c                                                                               
c      INPUT:  A -  matrix of logical dimensions m x n and physical             
c                   dimensions MP x NP  ** replaced by U on output)             
c                                                                               
c              M  - number of rows in A                                         
c              N  - number of columns in A                                      
c              MP - dimensions of array for storage of A                        
c              NP -  "                                                          
c      OUTPUT: A  -  matrix U                                                   
c              W  -  vectors of diagonal elements of matrix W                   
c              V  - matrix V                                                    
c                                                                               
c                                                                               
	 double precision a, w, v,rv1,f,g,h,c,s,x,y,z,scale,anorm                      
	 parameter (nmax =100)                                                         
	 dimension a(mp, np),w(np),v(np,np),rv1 (nmax)                                 
	 if (m.lt.n) then                                                              
	 write (6,*)' error - m < n '                                                  
	 stop 1                                                                        
	 endif                                                                         
	 g =0.                                                                         
	 scale =0.                                                                     
	 anorm=0.                                                                      
	 do 25 i=1,n                                                                   
	    l=i+1                                                                      
	    rv1(i) = scale * g                                                         
	    g = 0.                                                                     
	    s =0.                                                                      
	    scale =0.                                                                  
	    if (i.le.m) then                                                           
		do 11 k=i,m                                                                   
	        scale = scale + dabs(a(k,i))                                           
11     continue                                                                 
	   if (scale.ne.0.) then                                                       
	   do 12 k=i,m                                                                 
	   a(k,i)=a(k,i)/scale                                                         
	   s = s + a(k,i) *a(k,i)                                                      
12    continue                                                                  
	  f = a(i,i)                                                                   
	  g = - sign(sqrt(s),f)                                                        
	  h = f*g -s                                                                   
	  a(i,i) = f-g                                                                 
	  if (i.ne.n) then                                                             
	  do 15 j = l,n                                                                
	  s =0.                                                                        
	    do 13 k=i,m                                                                
	    s = s + a(k,i)*a(k,j)                                                      
13      continue                                                                
	  f=s/h                                                                        
	  do 14 k=i,m                                                                  
	  a(k,j)=a(k,j)+f*a(k,i)                                                       
14     continue                                                                 
15     continue                                                                 
	      endif                                                                    
	      do 16 k=i,m                                                              
	      a(k,i) = scale*a(k,i)                                                    
16    continue                                                                  
       endif                                                                    
       endif                                                                    
       w(i) = scale*g                                                           
       g =0.                                                                    
       s =0.                                                                    
       scale=0.                                                                 
       if ((i.le.m) .and. (i.ne.n) ) then                                       
       do 17 k=l,n                                                              
       scale = scale + dabs(a(i,k))                                             
17     continue                                                                 
       if (scale.ne.0.0) then                                                   
       do 18 k=l,n                                                              
       a(i,k) = a(i,k)/scale                                                    
       s = s+ a(i,k)*a(i,k)                                                     
18     continue                                                                 
       f=a(i,l)                                                                 
       g = - sign(sqrt(s),f)                                                    
       h = f*g -s                                                               
       a(i,l) = f-g                                                             
	  do 19 k = l,n                                                                
	  rv1(k)=a(i,k)/h                                                              
19       continue                                                               
	  if (i.ne.m) then                                                             
	  do 23 j=l,m                                                                  
	  s =0.                                                                        
	  do 21 k=l,n                                                                  
          s = s+ a(j,k)*a(i,k)                                                  
21     continue                                                                 
          do 22 k = l,n                                                         
	  a(j,k) = a(j,k) + s*rv1(k)                                                   
22        continue                                                              
23        continue                                                              
	  endif                                                                        
	  do 24 k=l,n                                                                  
  	  a(i,k) = scale*a(i,k)                                                      
24         continue                                                             
          endif                                                                 
	  endif                                                                        
	  anorm = max(anorm, (dabs(w(i)) +dabs(rv1(I))))                               
25        continue                                                              
c                                                                               
c  accumulation of right-hand transformations                                   
c                                                                               
	do 32 i = n,1,-1                                                               
	if (i.lt.n) then                                                               
	if (g.ne. 0.) then                                                             
	do 26 j = l, n                                                                 
	v(j,i) = (a(i,j)/a(i,l))/g                                                     
26     continue                                                                 
	 do 29 j=l,n                                                                   
	 s =0.                                                                         
	 do 27 k=l,n                                                                   
	 s = s +a(i,k) *v(k,j)                                                         
27       continue                                                               
		do 28 k=l,n                                                                   
		v(k,j)=v(k,j) + s*v(k,i)                                                      
28    continue                                                                  
29    continue                                                                  
       endif                                                                    
	  do 31 j = l,n                                                                
	  v(i,j) =0.                                                                   
	  v(j,i) =0.                                                                   
31      continue                                                                
	  endif                                                                        
	   v(i,i) =1.                                                                  
	   g = rv1(i)                                                                  
	   l=i                                                                         
32     continue                                                                 
c                                                                               
c    accumulation of left-hand transformations                                  
c                                                                               
c                                                                               
       do 39 i = n,1,-1                                                         
       l = i+1                                                                  
       g = w(i)                                                                 
       if (i.lt.n)then                                                          
	      do 33 j=l,n                                                              
	      a(i,j) = 0.                                                              
33    continue                                                                  
       endif                                                                    
       if (g.ne.0.)then                                                         
       g = 1.0/g                                                                
       if (i.ne.n) then                                                         
       do 36 j = l,n                                                            
       s =0.                                                                    
       do 34 k=l,m                                                              
       s = s + a(k,i)*a(k,j)                                                    
34     continue                                                                 
       f= (s/a(i,i))*g                                                          
	  do 35 k = i,m                                                                
          a(k,j) =  a(k,j)+ f*a(k,i)                                            
                                                                                
35     continue                                                                 
36     continue                                                                 
	 endif                                                                         
	 do 37 j = i,m                                                                 
         a(j,i) = a(j,i)*g                                                      
37        continue                                                              
       else                                                                     
	  do 38 j=i,m                                                                  
38	  a(j,i) =0                                                                  
          endif                                                                 
	  a(i,i) = a(i,i) + 1.                                                         
39        continue                                                              
c                                                                               
c   diagonalization of bidiagonal form                                          
c                                                                               
	do 49  k = n,1,-1                                                              
	do 48 its = 1, 30                                                              
	do 41 l  = k,1, -1                                                             
	 nm = l-1                                                                      
	 if ((dabs(rv1(l)) + anorm) .eq. anorm)go to 2                                 
	 if ((dabs(w(nm)) + anorm) .eq. anorm) go to 1                                 
41      continue                                                                
1        c =0                                                                   
	 s =1.                                                                         
	 do 43 i=l,k                                                                   
	  f = s * rv1(i)                                                               
	  if ((dabs(f)+anorm) .ne. anorm) then                                         
	   g= w(i)                                                                     
	   h = sqrt (f*f + g*g)                                                        
	   w(i) =h                                                                     
	   h= 1.0/h                                                                    
	   c = (g*h)                                                                   
	   s = -(f*h)                                                                  
	   do 42 j=1,m                                                                 
	   y = a(j,nm)                                                                 
	   z = a(j,i)                                                                  
	   a(j,nm) = (y*c) + (z*s)                                                     
	   a(j,i) = - (y*s) + z*c                                                      
42       continue                                                               
	      endif                                                                    
43     continue                                                                 
2        z = w(k)                                                               
       if (l.eq.k) then                                                         
       if (z.lt.0.) then                                                        
       w(k) = -z                                                                
       do 44 j = 1,n                                                            
         v(j,k) = -v(j,k)                                                       
44       continue                                                               
       endif                                                                    
       go to 3                                                                  
       endif                                                                    
       if (its.eq. 50) then                                                     
       write (6,*) ' error - no convergence in 50 iterations'                   
       stop 2                                                                   
        endif                                                                   
	x = w(l)                                                                       
	nm = k-1                                                                       
	y = w(nm)                                                                      
	g = rv1(nm)                                                                    
	h = rv1(k)                                                                     
	f = ((y-z)*(y+z) + (g-h)*(g+h))/(2.0*h*y)                                      
	g = sqrt (f*f +1.0)                                                            
	f = ((x-z)*(x+z)+h*((y/(f+sign(g,f)))-h))/x                                    
c                                                                               
c  next qr transformation                                                       
c                                                                               
       c = 1.0                                                                  
       s = 1.0                                                                  
	do 47 j = l,nm                                                                 
	i = j+1                                                                        
	g = rv1(i)                                                                     
	y = w(i)                                                                       
	h = s*g                                                                        
	g =c*g                                                                         
	z = sqrt (f*f+h*h)                                                             
	  rv1(j)=z                                                                     
	  c = f/z                                                                      
	  s = h/z                                                                      
	  f = (x*c)+  (g*s)                                                            
	  g =- (x*s) + (g*c)                                                           
	  h = y*s                                                                      
          y = y*c                                                               
	  do 45 jj =1,n                                                                
	  x = v(jj,j)                                                                  
	  z = v(jj,i)                                                                  
	  v(jj,j) = (x*c) +  (z*s)                                                     
	  v(jj,i) = -(x*s) +z*c                                                        
45      continue                                                                
	  z = sqrt(f*f + h*h)                                                          
	  w(j) = z                                                                     
	  if (z.ne.0.)then                                                             
	  z = 1.0/z                                                                    
	  c = f*z                                                                      
	  s = h*z                                                                      
	  endif                                                                        
	  f = (c*g) + (s*y)                                                            
	  x = - (s*g) + (c*y)                                                          
	  do 46 jj = 1,m                                                               
	  y = a(jj,j)                                                                  
	  z = a(jj,i)                                                                  
	  a(jj,j)=(y*c) + (z*s)                                                        
	  a(jj,i)=-(y*s) + (z*c)                                                       
46         continue                                                             
47       continue                                                               
         rv1(l) =0.                                                             
         rv1(k) = f                                                             
         w(k) = x                                                               
48     continue                                                                 
3      continue                                                                 
49     continue                                                                 
       return                                                                   
       end                                                                      
