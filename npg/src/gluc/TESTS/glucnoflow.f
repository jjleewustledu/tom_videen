         SUMS =0.
         DO 25 I=1,NPOINT
         SUMS = SUMS + WEIGHTS(I)*(TISSUE(I)-TISACT(I))**2
 25       CONTINUE
           FN = NPOINT - NPARM
           RMSE = SQRT(SUMS/FN)
 1082   FORMAT (/' WEIGHTED SUM-OF-SQUARES & RMSE ',2F20.3)
C
